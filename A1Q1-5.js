// QUESTION 2: FUNCTION WITH PARAMETERS

function doubleNumber(a){
    return a*2;
}
console.log(doubleNumber(4));





// QUESTION 3: SIMPLE RETURN FUNCTION

function addTwoNumbers(c,d){
    return c+d;
}
console.log(addTwoNumbers(2,4));




// QUESTION 4: ARROW FUNCTION

const doubleNumber=(a)=>a*2;
console.log(doubleNumber(4));




